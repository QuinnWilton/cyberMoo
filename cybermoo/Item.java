package cybermoo;

/**
 * Acts as a base class for all items
 * @author Shane
 */

public class Item {
    private String name;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

}
